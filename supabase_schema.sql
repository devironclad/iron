-- ==============================================================================
-- 2. MAIN TABLE: ls_assets
-- ==============================================================================

CREATE TABLE ls_assets (
    -- Identification (autonumber as per request)
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    ref_id NUMERIC,
    
    -- Foreign Keys to Auxiliary Tables
    origem_id UUID REFERENCES ls_origem(id) ON DELETE SET NULL,
    status_id UUID REFERENCES ls_status(id) ON DELETE SET NULL,
    priority_id UUID REFERENCES ls_priority(id) ON DELETE SET NULL,
    county_id UUID REFERENCES ls_county(id) ON DELETE SET NULL,
    auction_type_id UUID REFERENCES ls_auction_type(id) ON DELETE SET NULL,
    auction_model_id UUID REFERENCES ls_auction_model(id) ON DELETE SET NULL,
    property_type_id UUID REFERENCES ls_property_type(id) ON DELETE SET NULL,
    fema_id UUID REFERENCES ls_fema(id) ON DELETE SET NULL,
    wetlands_id UUID REFERENCES ls_wetlands(id) ON DELETE SET NULL,
    debit_id UUID REFERENCES ls_debit(id) ON DELETE SET NULL,
    gismap_id UUID REFERENCES ls_gismap(id) ON DELETE SET NULL,
    prop_access_id UUID REFERENCES ls_property_access(id) ON DELETE SET NULL,
    road_access_id UUID REFERENCES ls_road_access(id) ON DELETE SET NULL,
    ref_construction_id UUID REFERENCES ls_ref_construction(id) ON DELETE SET NULL,

    -- Legal & Location
    record_type VARCHAR(50) DEFAULT 'AUCTION',
    address VARCHAR(255),
    parcel_number VARCHAR(255),
    case_number VARCHAR(255),
    legal_description VARCHAR(255),
    coordinates VARCHAR(255),
    zoning VARCHAR(255),
    
    -- Metrics & Links
    size NUMERIC,
    link_regrid TEXT,
    observation TEXT,
    surrounds TEXT,
    link_sources TEXT,
    link_house_sources TEXT,
    link_video TEXT,
    link_earth TEXT,
    
    -- Financials & Bids
    market_value NUMERIC,
    annual_tax NUMERIC,
    open_bid NUMERIC,
    max_bid NUMERIC,
    max_bid_internal NUMERIC,
    min_bid NUMERIC,
    
    -- Appraisals
    appraisal_min NUMERIC,
    appraisal_avg NUMERIC,
    appraisal_max NUMERIC,
    county_appraisal NUMERIC,
    online_appraisal NUMERIC,
    sqft_price_reference NUMERIC,
    house_price NUMERIC,
    
    -- Dates (Stored with timezone for safety)
    auction_date TIMESTAMP WITH TIME ZONE,
    upset_date TIMESTAMP WITH TIME ZONE,
    acquisition_date TIMESTAMP WITH TIME ZONE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    
    -- Audit & Control (References Supabase Auth Users)
    created_by UUID REFERENCES auth.users(id) ON DELETE SET NULL,
    updated_by UUID REFERENCES auth.users(id) ON DELETE SET NULL,
    
    -- Booleans
    inperson_visit BOOLEAN DEFAULT FALSE,
    corner_lot BOOLEAN DEFAULT FALSE
);


-- ==============================================================================
-- 3. AUTOMATIC UPDATED_AT TRIGGER
-- ==============================================================================

CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER tr_ls_assets_updated_at
BEFORE UPDATE ON ls_assets
FOR EACH ROW
EXECUTE FUNCTION set_updated_at();


